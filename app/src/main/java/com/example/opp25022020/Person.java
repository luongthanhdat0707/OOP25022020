package com.example.opp25022020;

public class Person {
    private String name;
    private int age;
    private String adrees;
    private int phone;
    private int city_id;

    //alt + insert : hiện thị các hộp thoại đã tạo

    //Contructor:
    public Person(String name, int age, String adrees, int phone, int city_id) {
        this.name = name;
        this.age = age;
        this.adrees = adrees;
        this.phone = phone;
        this.city_id = city_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getAdrees() {
        return adrees;
    }

    public void setAdrees(String adrees) {
        this.adrees = adrees;
    }

    public int getPhone() {
        return phone;
    }

    public void setPhone(int phone) {
        this.phone = phone;
    }

    public int getCity_id() {
        return city_id;
    }

    public void setCity_id(int city_id) {
        this.city_id = city_id;
    }
}
